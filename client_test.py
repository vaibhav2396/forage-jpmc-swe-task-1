  def test_getRatio(self):
    prices = [
     {'price_a': 119.3, 'price_b': 234.5},
     {'price_a': 78.8, 'price_b': 34.1},
     {'price_a': 0, 'price_b': 234.5}
    ]

    for price in prices:
      self.assertEquals(getRatio(price['price_a'], price['price_b']), price['price_a'] / price['price_b'])
  def test_getRatio_calculateRatioPriceBisZero(self):
    prices = [
     {'price_a': 123, 'price_b': 0}
    ]

    for price in prices:
      self.assertEquals(getRatio(price['price_a'], price['price_b']), None)


